public class Euclidean {
    
     // Horizontal and VerticalDistance
    private final double hVDistance = 1.0; 
    // Diagonal Distance
    private final double dDistance = 1.4; 

    public Euclidean() {
    }

    /**
     * @return the hVDistance
     */
    public double gethVDistance() {
        return hVDistance;
    }

    /**
     * @return the dDistance
     */
    public double getdDistance() {
        return dDistance;
    }
    
    
    
}
