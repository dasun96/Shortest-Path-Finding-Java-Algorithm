public class Manhattan {
    //for Manhattan Distances,
    private final double horizontalVerticalDistance = 1.0;
    private final double diagonalDistance = 2.0;

    public Manhattan() {
    }

    /**
     * @return the horizontalVerticalDistance
     */
    public double getHorizontalVerticalDistance() {
        return horizontalVerticalDistance;
    }

    
    /**
     * @return the diagonalDistance
     */
    public double getDiagonalDistance() {
        return diagonalDistance;
    }

    
    
}
